// ES5
var myCars = ["BMW", "Tesla", "Toyota"]

for (var i = 0; i < myCars.length; i++) {
    console.log("My Cars (var): " + myCars[i])
}

console.log(i)

// ES6
let myCarsLet = ["BMW", "Tesla", "Toyota"]

for (let x = 0; x < myCarsLet.length; x++) {
    console.log("My Cars (let): " + myCarsLet[x])
}

console.log(x)